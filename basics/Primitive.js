// Primitives in JavaScript
// Primitives are the most basic data types in JavaScript.
// They are immutable and represent a single value.
// There are 7 primitive data types in JavaScript:


// 1. String: Represents a sequence of characters.
let str = "Hello, World!";

// 2. Number: Represents both integer and floating-point numbers.
let num = 42;

// 3. BigInt: Represents integers with arbitrary precision.
let bigInt = 1234567890123456789012345678901234567890n;

// 4. Boolean: Represents a logical entity and can have two values: true and false.
let isTrue = true;

// 5. Undefined: Represents a variable that has been declared but has not yet been assigned a value.
let undef;

// 6. Null: Represents the intentional absence of any object value.
let emptyValue = null;

// 7. Symbol: Represents a unique and immutable value, often used as object property keys.
let uniqueSymbol = Symbol("unique");

